    private void update() {
        try {
            Collection<CFparentesco> f = new HashSet<>(daocfpar.getAll());
            DefaultTableModel parTableModel = new DefaultTableModel();
            parTableModel.addColumn("ID");
            parTableModel.addColumn("Parentesco");
            for (CFparentesco p : f)
                parTableModel.addRow(new Object[]{p.getId(), p.getDescricao()});
            this.jTable2.setModel(parTableModel);
        } catch (SQLException ex) {
            Logger.getLogger(Adicionar_Membro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
